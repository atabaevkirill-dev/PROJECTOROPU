def find_font(paths):
    """Ищет первый существующий TTF-шрифт из списка."""
    for path in paths:
        if os.path.exists(path):
            return path
    return None


class ManualPDF(FPDF):
    """Класс PDF-документа с колонтитулами и удобными блоками текста."""

    def header(self):
        # На первой титульной странице шапка не нужна
        if self.page_no() == 1:
            return

        self.set_font("Main", "B", 8)
        self.set_text_color(120, 120, 120)
        self.cell(
            0,
            6,
            "PROJECTOR OPU Control — руководство пользователя интерфейса",
            0,
            1,
            "R",
        )
        self.ln(1)

    def footer(self):
        self.set_y(-15)
        self.set_font("Main", "", 8)
        self.set_text_color(120, 120, 120)
        self.cell(0, 8, f"Страница {self.page_no()}", 0, 0, "C")

    def ensure_space(self, height=12):
        """Добавляет новую страницу, если блок не помещается."""
        if self.get_y() + height > self.h - 18:
            self.add_page()

    def h1(self, text):
        self.ensure_space(16)
        self.ln(2)
        self.set_font("Main", "B", 14)
        self.set_text_color(20, 20, 20)
        self.multi_cell(0, 7, text)
        self.ln(1)

    def h2(self, text):
        self.ensure_space(14)
        self.ln(1)
        self.set_font("Main", "B", 12)
        self.set_text_color(40, 40, 40)
        self.multi_cell(0, 6, text)
        self.ln(1)

    def p(self, text):
        self.ensure_space(10)
        self.set_font("Main", "", 10.5)
        self.set_text_color(0, 0, 0)
        self.multi_cell(0, 5.6, text)
        self.ln(1.2)

    def b(self, text):
        self.ensure_space(10)
        self.set_font("Main", "", 10.5)
        self.set_text_color(0, 0, 0)
        self.cell(5, 5.6, "•")
        self.multi_cell(0, 5.6, text)
        self.ln(0.7)


